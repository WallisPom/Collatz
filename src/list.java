
public class list {
	private int mValue;
	private int mIterations;
	
	
	list(int value, int iterations){
		this.mValue = value;
		this.mIterations = iterations;
}
	

	public int getValue(){
			return this.mValue;
}
	public int getIterations(){
			return this.mIterations;
}


	public void setValue(int value) {
			this.mValue = value;
}

	public void setIterations(int iterations) {
			this.mIterations = iterations;

	
	
}

	
}
